print("\n🔧 Expanding top 10 schemes in schemes_data.py ...")

sd = read("schemes_data.py")
count = 0

for slug, new_data in EXPANSIONS.items():
    # Find the scheme in SCHEMES list by slug
    # We need to find the scheme block and replace specific fields in the "hi" section
    
    # Strategy: use JSON approach - load schemes.json, find scheme, but we need to edit the Python source
    # Better: find and replace specific strings
    
    # Find the scheme's "hi" block
    slug_marker = f'"slug": "{slug}"'
    alt_marker = f'"slug":"{slug}"'
    
    pos = sd.find(slug_marker)
    if pos < 0:
        pos = sd.find(alt_marker)
    
    if pos < 0:
        print(f"  ⚠️  {slug} not found in schemes_data.py — skipping")
        continue
    
    # Find the "hi": { section after this slug
    hi_start = sd.find('"hi":', pos)
    if hi_start < 0 or hi_start > pos + 2000:
        hi_start = sd.find('"hi" :', pos)
    
    if hi_start < 0 or hi_start > pos + 2000:
        print(f"  ⚠️  {slug}: could not find 'hi' section — skipping")
        continue
    
    # Find the opening { of hi
    hi_brace = sd.find('{', hi_start)
    
    # Now we need to find the end of the hi block
    # Count braces to find matching }
    depth = 0
    hi_end = hi_brace
    for i in range(hi_brace, len(sd)):
        if sd[i] == '{':
            depth += 1
        elif sd[i] == '}':
            depth -= 1
            if depth == 0:
                hi_end = i + 1
                break
    
    if hi_end <= hi_brace:
        print(f"  ⚠️  {slug}: could not find end of hi block — skipping")
        continue
    
    # Parse the current hi block
    hi_block_str = sd[hi_brace:hi_end]
    
    try:
        # Try to parse as JSON (should work since schemes_data.py uses JSON-compatible Python dicts)
        # But Python uses single quotes sometimes... let's use ast
        import ast
        current_hi = ast.literal_eval(hi_block_str)
    except:
        print(f"  ⚠️  {slug}: could not parse hi block — skipping")
        continue
    
    # Update with expanded content
    for key in new_data:
        current_hi[key] = new_data[key]
    
    # Convert back to Python dict string
    # Use repr but format it nicely
    new_hi_str = "{\n"
    for k, v in current_hi.items():
        new_hi_str += f'   "{k}": {repr(v)},\n'
    new_hi_str += "  }"
    
    # Replace in source
    sd = sd[:hi_brace] + new_hi_str + sd[hi_end:]
    count += 1
    print(f"  ✅ {slug} — expanded (intro, benefits, eligibility, docs, howToApply, {len(new_data.get('faqs',[]))} FAQs)")

write("schemes_data.py", sd)
print(f"\n📊 Expanded {count}/10 schemes")

